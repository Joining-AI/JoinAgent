# 这是一个模块，里面定义了一个关于"社区"列表的描述。

# "Communities" 是一个列表，但这个列表有点特别，它只装特定的东西。
# 每个列表里的元素都是一个三元组，每个三元组包含：
#   1. 一个整数（int）
#   2. 一个字符串（str）
#   3. 另一个装着字符串的列表（list[str]）

# 所以，想象一下，这个"Communities"就像一个小区的目录，每个小区有它的编号（整数）、名字（字符串）和里面的居民名字列表（字符串列表）。

